// Add function
function add(number1, number2) {
    return number1 + number2;
}
// Substract function
function substract(number1, number2) {
    return number1 - number2;
}
// Multiply function
function multiply(number1, number2) {
    return number1 * number2;
}
// Divid function
function divid(number1, number2) {
    return number1 / number2;
}

